package com.haod.www.chatroot.base;

public class Translation {

    /**
     * code : 100000
     * text : 你好呀，小主人，你长得好可爱呀。
     */

    private int code;
    private String text;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
